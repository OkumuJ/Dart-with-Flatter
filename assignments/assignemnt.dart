void main() {
  // int: Integer data type, used to represent whole numbers
  int age = 20;
  print('Age: $age');

  // double: Floating-point number, used to represent fractional numbers
  double height = 5.9;
  print('Height: $height');

  // String: Sequence of characters, used to represent text
  String name = 'Okumu Joseph';
  print('Name: $name');

  // List: Ordered group of items
  List<int> scores = [85, 90, 95];
  print('Scores: $scores');

  // Map: Unordered collection of key-value pairs
  Map<String, int> fruitsInBasket = {
    'apples': 10,
    'oranges': 5,
  };
  print('Fruits in basket: $fruitsInBasket');
}
